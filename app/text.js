// Main.
async function appMain() {
	let pressing = 0;
	if (playing >= texts[state].length) {
		if (picoAction()) {
			state = state + 1 <= 6 ? state + 1: 0;
			playing = 0;
		} else if (picoMotion()) {
			pressing = 1;
		}
	} else {
		if (picoMotion()) {
			if (playing + 10 < texts[state].length - 1) {
				playing = playing + 10;
			} else {
				playing = texts[state].length - 1;
			}
		} else {
			playing += 1;
		}
		picoFlush();
	}

	picoClear();
	picoText(labels[state], -1, 0,-88,152,8, 0,1)
	picoRect(0, 0,-40,160,80, 0,1);
	picoText(figures[state], -1, 0,-40,152,72, 0,1);
	picoText(texts[state].substr(0,playing-pressing), -1, 0,48,152,88, 0,1);
}
